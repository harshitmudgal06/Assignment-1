# Q3. Python program for user name input, uppercase, and length

# Ans.
# getting name from the user
user_name = input("Enter your name: ")          ####👉👉 Takes user name as input

# changing the name to uppercase and printing it
print("Name in uppercase:", user_name.upper())      ####👉👉 prints user name in upper case

# counting the characters using len() function
print("Total number of characters:", len(user_name))    ####👉👉 prints total no. of characters in user name 
