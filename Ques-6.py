# Q6. Python program to modify a list of numbers

#Ans.
# creating list of numbers:
numbers = [10, 20, 30, 40, 50]

# adding 60 to the end of the list
numbers.append(60)      ####👉👉 add 60 in the last position in list.

# removing 20 from the list
numbers.remove(20)      ####👉👉 removes 20 from the list

# printing the final list to see the changes
print("Updated list:", numbers)     ####👉👉 Updated list will be: [10, 30, 40, 50, 60]